    <h2> Myers Briggs Personality Type</h2><br>
<p class="bigger">INTP - Logician</p>
<p>Seek to develop logical explanations for everything that interests them. Theoretical and abstract, interested more in ideas than in social interaction. Quiet, contained, flexible,
	 and adaptable. Have unusual ability to focus in depth to solve problems in their area of interest. Skeptical, sometimes critical, always analytical.</p>
<p><cite><a href="http://www.myersbriggs.org/my-mbti-personality-type/mbti-basics/the-16-mbti-types.htm">  Myers &amp; Briggs Foundation</a></cite>

<p>The INTP personality type is fairly rare, making up only three percent of the population, which is definitely a good thing for them, as there’s nothing they’d be more unhappy
	 about than being "common". INTPs pride themselves on their inventiveness and creativity, their unique perspective and vigorous intellect. Usually known as the philosopher, the
	 architect, or the dreamy professor, INTPs have been responsible for many scientific discoveries throughout history.</p>
<p>They love patterns, and spotting discrepancies between statements could almost be described as a hobby, making it a bad idea to lie to an INTP. This makes it ironic that INTPs’ word
	 should always be taken with a grain of salt – it’s not that they are dishonest, but people with the INTP personality type tend to share thoughts that are not fully developed, using
	 others as a sounding board for ideas and theories in a debate against themselves rather than as actual conversation partners.</p>
<p><cite><a href="https://www.16personalities.com/intp-personality"> 16 Personalities</a></cite>
    <br><br>
<p> Different tests have given me different results. I am close to an INFP in some aspects, but feel like I am probably closer to an INTP.</p>
    <br>
<p class="bigger">INFP - Mediator</p>
<p>Idealistic, loyal to their values and to people who are important to them. Want an external life that is congruent with their values. Curious, quick to see possibilities,
	 can be catalysts for implementing ideas. Seek to understand people and to help them fulfill their potential. Adaptable, flexible, and accepting unless a value is threatened.</p>
<p><cite><a href="http://www.myersbriggs.org/my-mbti-personality-type/mbti-basics/the-16-mbti-types.htm">  Myers &amp; Briggs Foundation</a></cite>

	<p>INFP personalities are true idealists, always looking for the hint of good in even the worst of people and events, searching for ways to make things better. While they may be perceived as calm,
		 reserved, or even shy, INFPs have an inner flame and passion that can truly shine. Comprising just 4% of the population, the risk of feeling misunderstood is unfortunately high for the INFP
		 personality type – but when they find like-minded people to spend their time with, the harmony they feel will   be a fountain of joy and inspiration.</p>
	<p>Being a part of the Diplomat personality group, INFPs are guided by their principles, rather than by logic (Analysts), excitement (Explorers), or practicality (Sentinels).
		 When deciding how to move forward, they will look to honor, beauty, morality and virtue – INFPs are led by the purity of their intent, not rewards and punishments. People who share the
		 INFP personality type are proud of this quality, and rightly so, but not everyone understands the drive behind these feelings, and it can lead to isolation.</p>
